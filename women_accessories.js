    let womendata = document.getElementById("box");
    let arrLS = JSON.parse(localStorage.getItem("cartData")) || [];

    function displayProducts(wadata) {
        womendata.innerHTML = "";
        wadata.forEach(function (elem) {
            let product = document.createElement("div");
            let product1 = document.createElement("div");
            let product2 = document.createElement("div");
            let product3 = document.createElement("div");

            let img = document.createElement("img");
            img.src = elem.img;

            let name = document.createElement("p")
            name.innerText = elem.brand

            let product_id = document.createElement("p")
            product_id.innerText = elem.product_id

            let category = document.createElement("p")
            category.innerText = elem.category

            let price = document.createElement("h3")
            price.innerText = "$" + elem.price

            let btn = document.createElement("button")
            btn.innerText = "Add To Cart"

            btn.addEventListener("click",function(){
                if(addtocartfunc(elem.product_id)==true){
                 alert("Product added Successfully");
                 arrLS.push(elem);
                 localStorage.setItem("cartData",JSON.stringify(arrLS));
                }
                else{
                  alert("Product already in the cart");
                }
              })
              elem.quant=1;
            product1.append(img)
            product2.append(name, product_id,category,price)
            product3.append(btn)
            product.append(product1, product2, product3);
            womendata.append(product)

        })
        
    }
    displayProducts(wadata)

function addtocartfunc(product_id){
        for(let i=0; i<arrLS.length; i++){
        if(arrLS[i].product_id===product_id){
        return false;
        }
        }
        return true;
          }
          
