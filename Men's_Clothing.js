let mendata = document.getElementById( "box" );
let menClothingLS = JSON.parse( localStorage.getItem( "cartData" ) ) || [];


function displayProducts( mc_prod_data ) {
    mendata.innerHTML = "";
    mc_prod_data.forEach( function ( elem ) {
        let product = document.createElement( "div" );
        let product1 = document.createElement( "div" );
        let product2 = document.createElement( "div" );
        let product3 = document.createElement( "div" );

        let img = document.createElement( "img" );
        img.src = elem.img;

        let name = document.createElement( "p" )
        name.innerText = elem.brand

        let product_name = document.createElement( "p" )
        product_name.innerText = elem.category

        let price = document.createElement( "h3" )
        price.innerText = "$" + elem.price

        let btn = document.createElement( "button" )
        btn.innerText = "Add To Cart"
        btn.addEventListener( "click", function () {
            if ( btnFunc( elem.product_id ) == true ) {
                alert( "Product added Successfully" );
                menClothingLS.push( elem );
                // console.log(menClothingLS);
                localStorage.setItem( "cartData", JSON.stringify( menClothingLS ) )
            } else {
                alert( "Product already in the cart" )
            }
        } )
        elem.quant = 1;
        product1.append( img )
        product2.append( name, product_name, price )
        product3.append( btn )
        product.append( product1, product2, product3 );
        mendata.append( product )

    } )
    // localStorage.setItem("cartData",JSON.stringify(mc_prod_data))
}
displayProducts( mc_prod_data )


document.querySelector( "#Category" ).addEventListener( "change", function () {
    let val = document.querySelector( "#Category" ).value;
    if ( val == "All" ) {
        displayProducts( mc_prod_data );
    }
    else {
        let filtered = mc_prod_data.filter( function ( elem ) {
            return elem.category == val;
        } )
        displayProducts( filtered );
    }
} )
document.querySelector( "#Brand" ).addEventListener( "change", function () {
    let val = document.querySelector( "#Brand" ).value;
    if ( val == "All" ) {
        displayProducts( mc_prod_data );
    }
    else {
        let filtered = mc_prod_data.filter( function ( elem ) {
            return elem.brand == val;
        } )
        displayProducts( filtered );
    }
} )
document.querySelector( "#SortPrice" ).addEventListener( "change", function () {
    let val = document.querySelector( "#SortPrice" ).value;
    if ( val == "Ascending" ) {
        mc_prod_data.sort( function ( a, b ) {
            if ( a.price < b.price ) return -1
            if ( a.price > b.price ) return 1
            return 0;
        } )
        displayProducts( mc_prod_data );;
    }
    else if ( val == "Descending" ) {
        mc_prod_data.sort( function ( a, b ) {
            if ( a.price < b.price ) return 1
            if ( a.price > b.price ) return -1
            return 0;
        } )
        displayProducts( mc_prod_data );;
    }
} )
function btnFunc( id ) {
    for ( let i = 0; i < menClothingLS.length; i++ ) {
        if ( menClothingLS[ i ].product_id == id ) {
            return false;
        }
    }
    return true;
}
